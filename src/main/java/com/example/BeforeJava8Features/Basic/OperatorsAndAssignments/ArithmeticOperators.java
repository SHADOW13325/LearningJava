public class ArithmeticOperators {

    public static void main(String[] args) {

        int a = 10, b = 20, c;
        c = a + b; // Addition
        c = a - b; // Subtraction
        c = a * b; // Multiplication
        c = b / a; // Division
        c = a % b; // Modulo . Only applicable for Integral Type
    }
}
